//! Compact 5x7 bitmap font (standard glcdfont values, column-major: each glyph is
//! 5 columns, bit r of a column = pixel at row r, top=bit0). Used to render the
//! Text channel to a device-sized RGB frame, then push via `show_image` — matching
//! the Python `LightingApi._render_text_png` static-image behavior (scrolling is a
//! follow-up). Uppercases input; unknown chars render blank.

/// 5 columns per glyph. Subset: space, A-Z, 0-9, common punctuation.
fn glyph(c: char) -> [u8; 5] {
    match c.to_ascii_uppercase() {
        ' ' => [0x00, 0x00, 0x00, 0x00, 0x00],
        '0' => [0x3E, 0x51, 0x49, 0x45, 0x3E],
        '1' => [0x00, 0x42, 0x7F, 0x40, 0x00],
        '2' => [0x42, 0x61, 0x51, 0x49, 0x46],
        '3' => [0x21, 0x41, 0x45, 0x4B, 0x31],
        '4' => [0x18, 0x14, 0x12, 0x7F, 0x10],
        '5' => [0x27, 0x45, 0x45, 0x45, 0x39],
        '6' => [0x3C, 0x4A, 0x49, 0x49, 0x30],
        '7' => [0x01, 0x71, 0x09, 0x05, 0x03],
        '8' => [0x36, 0x49, 0x49, 0x49, 0x36],
        '9' => [0x06, 0x49, 0x49, 0x29, 0x1E],
        'A' => [0x7E, 0x11, 0x11, 0x11, 0x7E],
        'B' => [0x7F, 0x49, 0x49, 0x49, 0x36],
        'C' => [0x3E, 0x41, 0x41, 0x41, 0x22],
        'D' => [0x7F, 0x41, 0x41, 0x22, 0x1C],
        'E' => [0x7F, 0x49, 0x49, 0x49, 0x41],
        'F' => [0x7F, 0x09, 0x09, 0x09, 0x01],
        'G' => [0x3E, 0x41, 0x49, 0x49, 0x7A],
        'H' => [0x7F, 0x08, 0x08, 0x08, 0x7F],
        'I' => [0x00, 0x41, 0x7F, 0x41, 0x00],
        'J' => [0x20, 0x40, 0x41, 0x3F, 0x01],
        'K' => [0x7F, 0x08, 0x14, 0x22, 0x41],
        'L' => [0x7F, 0x40, 0x40, 0x40, 0x40],
        'M' => [0x7F, 0x02, 0x0C, 0x02, 0x7F],
        'N' => [0x7F, 0x04, 0x08, 0x10, 0x7F],
        'O' => [0x3E, 0x41, 0x41, 0x41, 0x3E],
        'P' => [0x7F, 0x09, 0x09, 0x09, 0x06],
        'Q' => [0x3E, 0x41, 0x51, 0x21, 0x5E],
        'R' => [0x7F, 0x09, 0x19, 0x29, 0x46],
        'S' => [0x46, 0x49, 0x49, 0x49, 0x31],
        'T' => [0x01, 0x01, 0x7F, 0x01, 0x01],
        'U' => [0x3F, 0x40, 0x40, 0x40, 0x3F],
        'V' => [0x1F, 0x20, 0x40, 0x20, 0x1F],
        'W' => [0x7F, 0x20, 0x18, 0x20, 0x7F],
        'X' => [0x63, 0x14, 0x08, 0x14, 0x63],
        'Y' => [0x03, 0x04, 0x78, 0x04, 0x03],
        'Z' => [0x61, 0x51, 0x49, 0x45, 0x43],
        '-' => [0x08, 0x08, 0x08, 0x08, 0x08],
        '+' => [0x08, 0x08, 0x3E, 0x08, 0x08],
        '.' => [0x00, 0x60, 0x60, 0x00, 0x00],
        ':' => [0x00, 0x36, 0x36, 0x00, 0x00],
        '!' => [0x00, 0x00, 0x5F, 0x00, 0x00],
        '?' => [0x02, 0x01, 0x51, 0x09, 0x06],
        '/' => [0x20, 0x10, 0x08, 0x04, 0x02],
        _ => [0x00, 0x00, 0x00, 0x00, 0x00],
    }
}

const GW: usize = 5; // glyph width
const GH: usize = 7; // glyph height
const ADV: usize = 6; // per-char advance (5 + 1 spacing)

/// Render `text` centered on a `size`×`size` black RGB frame in `color`. Text
/// wider than the frame is clipped (scrolling is a follow-up, matching Python's
/// current static push).
pub fn render(text: &str, color: [u8; 3], size: usize) -> Vec<[u8; 3]> {
    let mut frame = vec![[0u8, 0, 0]; size * size];
    let text: Vec<char> = text.chars().filter(|c| !c.is_control()).collect();
    let total_w = text.len().saturating_mul(ADV).saturating_sub(1);
    let x0 = if total_w < size { (size - total_w) / 2 } else { 0 } as isize;
    let y0 = ((size as isize) - GH as isize) / 2;
    for (i, &c) in text.iter().enumerate() {
        let g = glyph(c);
        let cx = x0 + (i * ADV) as isize;
        for (col, bits) in g.iter().enumerate() {
            let px = cx + col as isize;
            if px < 0 || px >= size as isize {
                continue;
            }
            for row in 0..GH {
                if (bits >> row) & 1 == 1 {
                    let py = y0 + row as isize;
                    if py >= 0 && py < size as isize {
                        frame[py as usize * size + px as usize] = color;
                    }
                }
            }
        }
    }
    frame
}

#[cfg(test)]
mod tests {
    use super::*;
    #[test]
    fn renders_nonblank_for_letters() {
        let f = render("A", [255, 255, 255], 16);
        assert_eq!(f.len(), 256);
        assert!(f.iter().any(|p| *p != [0, 0, 0]), "A should light some pixels");
    }
    #[test]
    fn space_is_blank() {
        let f = render("   ", [255, 255, 255], 16);
        assert!(f.iter().all(|p| *p == [0, 0, 0]));
    }
}
